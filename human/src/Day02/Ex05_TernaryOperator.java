package Day02;

public class Ex05_TernaryOperator {
	
	public static void main(String[] args) {
		int a = 3, b = 5;
		
		// a 와 b 두수으 차이를  구하시오
	
		// (조건) ? : (참일때 문장) : (거짓일때 문장)
		
		int result = (a > b) ? a-b : b-a;
		System.out.println("두 수의 차 : " + result);
		
		
		
		
	}
	

}
